day = input ("Enter day \n")
mouth = input ("Enter mouth \n")
year = input ("Enter year \n")
if day > 0 and day < 32:
    print ("Day entered correct")
else:
    print ("Day entered incorrect")

if mouth > 0 and mouth < 13:
    print ("Mouth entered correct")
else:
    print ("Mouth entered incorrect")

if year > 0 and year < 2019:
    print ("Year entered correct")
else:
    print ("Year entered incorrect")
